-- -------------------------------------------
-- 3. 缺陷表（面试核心表）
-- -------------------------------------------
CREATE TABLE bugs (
    id INT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(200) NOT NULL,
    severity ENUM('致命','严重','一般','轻微') NOT NULL,
    status ENUM('新建','已分配','已修复','已关闭','重新打开') DEFAULT '新建',
    project_id INT,
    tester_id INT,        -- 发现人
    assignee_id INT,       -- 修复人/处理人
    created_date DATE,
    closed_date DATE,
    FOREIGN KEY (project_id) REFERENCES projects(id),
    FOREIGN KEY (tester_id) REFERENCES testers(id),
    FOREIGN KEY (assignee_id) REFERENCES testers(id)
);

