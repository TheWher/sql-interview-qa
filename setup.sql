-- -------------------------------------------
-- 2. 测试人员表
-- -------------------------------------------
CREATE TABLE testers (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(20) NOT NULL,
    level ENUM('初级','中级','高级') DEFAULT '初级',
    project_id INT,
    FOREIGN KEY (project_id) REFERENCES projects(id)
