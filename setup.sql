DROP DATABASE IF EXISTS qa_interview;
CREATE DATABASE qa_interview;
USE qa_interview;

-- -------------------------------------------
-- 1. 项目表
-- -------------------------------------------
CREATE TABLE projects (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(50) NOT NULL,
    status ENUM('进行中','已上线','维护中') DEFAULT '进行中',
    start_date DATE
);

